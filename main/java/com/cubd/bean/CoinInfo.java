package com.cubd.bean;

public class CoinInfo {

    private String coinType;
    private String coinCHNName;
    private double coinRate;

    public String getCoinType() {
        return coinType;
    }

    public void setCoinType(String coinType) {
        this.coinType = coinType;
    }

    public String getCoinCHNName() {
        return coinCHNName;
    }

    public void setCoinCHNName(String coinCHNName) {
        this.coinCHNName = coinCHNName;
    }

    public double getCoinRate() {
        return coinRate;
    }

    public void setCoinRate(double coinRate) {
        this.coinRate = coinRate;
    }

}
